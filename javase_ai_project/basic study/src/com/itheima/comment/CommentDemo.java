package com.itheima.comment;

/**
 * 3.文档注释
 * 这是一个程序，用来解释注释的用法
 */
public class CommentDemo {
    /**
     * 这是一个main方法，是程序的入口
     * 必须有main方法，才能运行程序
     */
    public static void main(String[] args) {
        //1.单行注释
        //打印两行诗
        System.out.println("春去秋来，江水共济");
        /*
           2.多行注释
           打印3行诗句依次打印
         */
        System.out.println("春去秋来，江水共济");
        System.out.println("春去秋来，江水共济");
        System.out.println("春去秋来，江水共济");
    }
}
